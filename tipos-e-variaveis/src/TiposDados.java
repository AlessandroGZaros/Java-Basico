public class TiposDados {
    public static void main(String[] args) {
        byte idade = 123;
        short ano = 2021;
        int cep = 21070333;//se começar com zero, talvez tenha que ser outro tipo(String)
        long cpf = 98765432109L;//se começar com zero, talvez tenha que ser outro tipo(String)
        //variável do tipo long necessita de colocar o l ou L no final da declaraçao
        float pi = 3.14F; // variável do tipo float necessita de colocar o f ou F no final da declaraçao
        double salario = 1275.44;
    }
}
